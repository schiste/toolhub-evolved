function repoRoot() {
	const out = spawnSync("git", ["rev-parse", "--show-toplevel"], { encoding: "utf8" });
	if (out.status !== 0) {
		console.error("quality: not inside a git checkout");
		process.exit(2);
	}
	return out.stdout.trim();
}

function gitFiles() {
	const out = spawnSync("git", ["ls-files", "--cached", "--others", "--exclude-standard", "-z"], {
		encoding: "utf8"
	});
	if (out.status !== 0) throw new Error(out.stderr || "git ls-files failed");
	return out.stdout
		.split("\0")
		.filter(Boolean)
		.filter((file) => existsSync(file) && statSync(file).isFile())
		.sort();
}

function fail(check, file, line, message) {
	failures.push({ check, file: file || null, line: line || null, message });
}

function read(file) {
	return readFileSync(file, "utf8");
}

function rel(file) {
	return file.split(path.sep).join("/");
}

function ext(file) {
	return path.extname(file);
}

function sourceFiles() {
	return files.filter((file) => SOURCE_EXTS.has(ext(file)));
}

function lineNumber(text, offset) {
	return text.slice(0, offset).split("\n").length;
}

function fileLineEntries(file, text) {
	return text.split(/\r?\n/).map((line, index) => ({ line, number: index + 1, file }));
}

function runCheck(name, fn) {
	const start = Date.now();
	const before = failures.length;
	try {
		fn(name);
	} catch (error) {
		fail(name, null, null, error && error.stack ? error.stack : String(error));
	}
	checks.push({ name, durationMs: Date.now() - start, failures: failures.length - before });
}

function command(check, cmd, args) {
	const out = spawnSync(cmd, args, { encoding: "utf8", cwd: root });
	if (out.status !== 0) {
		const output = [out.stdout, out.stderr].filter(Boolean).join("\n").trim();
		fail(check, args[args.length - 1], null, output || `${cmd} failed`);
	}
}

function bin(name) {
	return path.join("node_modules", ".bin", name);
}

function pyBin(name) {
	return path.join(".quality", "python", "bin", name);
}

function requireFile(check, file, hint) {
	if (!existsSync(file)) fail(check, file, null, hint);
}

