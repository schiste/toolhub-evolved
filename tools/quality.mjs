function splitSelectors(raw) {
	const selectors = [];
	let current = "";
	let depth = 0;
	let quote = "";
	for (const char of raw) {
		if (quote) {
			current += char;
			if (char === quote) quote = "";
			continue;
		}
		if (char === '"' || char === "'") {
			quote = char;
			current += char;
			continue;
		}
		if (char === "(" || char === "[") depth += 1;
		else if (char === ")" || char === "]") depth = Math.max(0, depth - 1);
		if (char === "," && depth === 0) {
			selectors.push(current.trim());
			current = "";
		} else {
			current += char;
		}
	}
	if (current.trim()) selectors.push(current.trim());
	return selectors;
}

