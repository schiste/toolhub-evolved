function importsFor(file) {
	const text = read(file);
	const imports = [];
	const regex = /(?:^|\n)(\s*import\s+([\S\s]*?)\s+from\s+["']([^"']+)["'];)/g;
	let match;
	while ((match = regex.exec(text))) {
		const index = match.index + (match[0].startsWith("\n") ? 1 : 0);
		imports.push({ spec: match[3], index, line: lineNumber(text, index) });
	}
	return imports;
}

function resolveImport(file, spec) {
	return rel(path.normalize(path.join(path.dirname(file), spec)));
}

function layerOf(file) {
	const name = rel(file);
	if (name === "public_html/main.js") return { layer: 5, label: "app" };
	if (name.startsWith("public_html/views/")) return { layer: 4, label: "views" };
	if (name.startsWith("public_html/lib/organisms/")) return { layer: 3, label: "organisms" };
	if (name.startsWith("public_html/lib/molecules/")) return { layer: 2, label: "molecules" };
	if (name.startsWith("public_html/lib/atoms/")) return { layer: 1, label: "atoms" };
	if (name.startsWith("public_html/lib/core/")) return { layer: 0, label: "core" };
	return { layer: -1, label: "other" };
}

function importAllowed(source, target) {
	const s = layerOf(source);
	const t = layerOf(target);
	if (s.layer === -1 || t.layer === -1) return true;
	if (source === "public_html/main.js") return true;
	if (source === "public_html/views/router.js" && target.startsWith("public_html/views/")) return true;
	return t.layer <= s.layer;
}

runCheck("imports and architecture", (check) => {
	const graph = new Map();
	const browserFiles = files.filter((item) => JS_EXTS.has(ext(item)) && rel(item).startsWith("public_html/"));
	for (const file of browserFiles) {
		const seen = new Set();
		const deps = [];
		for (const item of importsFor(file)) {
			if (seen.has(item.spec)) fail(check, file, item.line, `duplicate import from ${item.spec}`);
			seen.add(item.spec);
			if (!item.spec.startsWith(".")) {
				fail(check, file, item.line, `browser modules must use local relative imports; found ${item.spec}`);
				continue;
			}
			if (!item.spec.endsWith(".js")) {
				fail(check, file, item.line, `local imports must include the .js extension: ${item.spec}`);
			}
			const target = resolveImport(file, item.spec);
			if (!existsSync(target)) {
				fail(check, file, item.line, `import target does not exist: ${item.spec}`);
				continue;
			}
			if (target === rel(file)) fail(check, file, item.line, "self-import is not allowed");
			if (!importAllowed(rel(file), target)) {
				fail(
					check,
					file,
					item.line,
					`${layerOf(file).label} layer must not import upward from ${layerOf(target).label}`
				);
			}
			deps.push(target);
		}
		graph.set(rel(file), deps);
	}
	const visiting = new Set();
	const visited = new Set();
	const stack = [];
	function dfs(node) {
		if (visiting.has(node)) {
			fail(
				check,
				node,
				null,
				`circular import detected: ${[...stack.slice(stack.indexOf(node)), node].join(" -> ")}`
			);
			return;
		}
		if (visited.has(node)) return;
		visiting.add(node);
		stack.push(node);
		for (const dep of graph.get(node) || []) dfs(dep);
		stack.pop();
		visiting.delete(node);
		visited.add(node);
	}
	for (const node of graph.keys()) dfs(node);
});

function exportedNames(file) {
	const text = read(file);
	const names = new Set();
	for (const match of text.matchAll(/export\s+(?:async\s+)?function\s+([$A-Z_a-z][\w$]*)/g)) names.add(match[1]);
	for (const match of text.matchAll(/export\s+(?:const|let|var)\s+([$A-Z_a-z][\w$]*)/g)) names.add(match[1]);
	for (const match of text.matchAll(/export\s*{\s*([^}]+)\s*}/g)) {
		for (const raw of match[1].split(",")) {
			const exported = raw
				.trim()
				.split(/\s+as\s+/)
				.pop()
				.trim();
			if (exported) names.add(exported);
		}
	}
	return [...names].sort();
}

function matchingBraceEnd(text, start) {
	let depth = 0;
	let quote = "";
	let lineComment = false;
	let blockComment = false;
	let escaped = false;
	for (let index = start; index < text.length; index += 1) {
		const char = text[index];
		const next = text[index + 1];
		if (lineComment) {
			if (char === "\n") lineComment = false;
			continue;
		}
		if (blockComment) {
			if (char === "*" && next === "/") {
				blockComment = false;
				index += 1;
			}
			continue;
		}
		if (quote) {
			if (escaped) {
				escaped = false;
			} else if (char === "\\") {
				escaped = true;
			} else if (char === quote) {
				quote = "";
			}
			continue;
		}
		if (char === "/" && next === "/") {
			lineComment = true;
			index += 1;
			continue;
		}
		if (char === "/" && next === "*") {
			blockComment = true;
			index += 1;
			continue;
		}
		if (char === '"' || char === "'" || char === "`") {
			quote = char;
			continue;
		}
		if (char === "{") {
			depth += 1;
		} else if (char === "}") {
			depth -= 1;
			if (depth === 0) return index;
		}
	}
	return -1;
}

function extractObjectBody(text, marker) {
	const markerIndex = text.indexOf(marker);
	if (markerIndex === -1) return "";
	const start = text.indexOf("{", markerIndex);
	if (start === -1) return "";
	const end = matchingBraceEnd(text, start);
	return end === -1 ? "" : text.slice(start + 1, end);
}

function quotedKeyAt(body, index, quoteChar) {
	let end = index + 1;
	while (end < body.length) {
		if (body[end] === "\\" && end + 1 < body.length) {
			end += 2;
			continue;
		}
		if (body[end] === quoteChar) break;
		end += 1;
	}
	if (end >= body.length) return null;
	return { key: body.slice(index + 1, end), index: end };
}

function identifierKeyAt(body, index) {
	const match = /^[$A-Z_a-z][\w$-]*/.exec(body.slice(index));
	return match ? { key: match[0], index: index + match[0].length - 1 } : null;
}

function objectKeyAt(body, index) {
	const char = body[index];
	if (char === '"' || char === "'") return quotedKeyAt(body, index, char);
	return /[$A-Z_a-z]/.test(char) ? identifierKeyAt(body, index) : null;
}

function hasColonAfterKey(body, index) {
	let probe = index + 1;
	while (/\s/.test(body[probe] || "")) probe += 1;
	return body[probe] === ":";
}

function topLevelObjectKeys(body) {
	const keys = [];
	let depth = 0;
	let quote = "";
	let escaped = false;
	for (let index = 0; index < body.length; index += 1) {
		const char = body[index];
		if (quote) {
			if (escaped) escaped = false;
			else if (char === "\\") escaped = true;
			else if (char === quote) quote = "";
			continue;
		}

		if (depth === 0) {
			const candidate = objectKeyAt(body, index);
			if (candidate && hasColonAfterKey(body, candidate.index)) {
				keys.push(candidate.key);
				index = candidate.index;
				continue;
			}
		}

		if (char === '"' || char === "'" || char === "`") {
			quote = char;
			continue;
		}
		if (char === "{" || char === "(" || char === "[") depth += 1;
		else if (char === "}" || char === ")" || char === "]") depth = Math.max(0, depth - 1);
	}
	return [...new Set(keys)].sort();
}

function objectKeysFromConst(file, name) {
	const text = read(file);
	return [
		...topLevelObjectKeys(extractObjectBody(text, `const ${name} =`)),
		...topLevelObjectKeys(extractObjectBody(text, `export const ${name} =`))
	];
}

