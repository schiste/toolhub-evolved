runCheck("text hygiene", (check) => {
	for (const file of sourceFiles()) {
		const name = rel(file);
		const scanForbiddenText = name !== "tools/quality.mjs";
		const scanForbiddenCode = name.startsWith("public_html/") && CODE_EXTS.has(ext(file));
		const text = read(file);
		for (const entry of fileLineEntries(file, text)) {
			if (/^(<<<<<<<|=======|>>>>>>>)($| )/.test(entry.line)) {
				fail(check, file, entry.number, "merge conflict marker is not allowed");
			}
			if (scanForbiddenText) {
				for (const rule of FORBIDDEN_TEXT) {
					if (rule.pattern.test(entry.line)) fail(check, file, entry.number, rule.message);
				}
			}
			if (scanForbiddenCode) {
				for (const rule of FORBIDDEN_CODE) {
					if (rule.pattern.test(entry.line)) fail(check, file, entry.number, rule.message);
				}
			}
			const limit = TEXT_LINE_LIMITS.get(ext(file));
			if (limit && entry.line.length > limit) {
				fail(check, file, entry.number, `line is ${entry.line.length} chars; limit is ${limit}`);
			}
		}
	}
});

function importsFor(file) {
	const text = read(file);
	const imports = [];
	const regex = /(?:^|\n)(\s*import\s+([\S\s]*?)\s+from\s+["']([^"']+)["'];)/g;
	let match;
	while ((match = regex.exec(text))) {
		const index = match.index + (match[0].startsWith("\n") ? 1 : 0);
		imports.push({ spec: match[3], index, line: lineNumber(text, index) });
	}
	return imports;
}

function resolveImport(file, spec) {
	return rel(path.normalize(path.join(path.dirname(file), spec)));
}

function layerOf(file) {
	const name = rel(file);
	if (name === "public_html/main.js") return { layer: 5, label: "app" };
	if (name.startsWith("public_html/views/")) return { layer: 4, label: "views" };
	if (name.startsWith("public_html/lib/organisms/")) return { layer: 3, label: "organisms" };
	if (name.startsWith("public_html/lib/molecules/")) return { layer: 2, label: "molecules" };
	if (name.startsWith("public_html/lib/atoms/")) return { layer: 1, label: "atoms" };
	if (name.startsWith("public_html/lib/core/")) return { layer: 0, label: "core" };
	return { layer: -1, label: "other" };
}

function importAllowed(source, target) {
	const s = layerOf(source);
	const t = layerOf(target);
	if (s.layer === -1 || t.layer === -1) return true;
	if (source === "public_html/main.js") return true;
	if (source === "public_html/views/router.js" && target.startsWith("public_html/views/")) return true;
	return t.layer <= s.layer;
}

runCheck("imports and architecture", (check) => {
	const graph = new Map();
	const browserFiles = files.filter((item) => JS_EXTS.has(ext(item)) && rel(item).startsWith("public_html/"));
	for (const file of browserFiles) {
		const seen = new Set();
		const deps = [];
		for (const item of importsFor(file)) {
			if (seen.has(item.spec)) fail(check, file, item.line, `duplicate import from ${item.spec}`);
			seen.add(item.spec);
			if (!item.spec.startsWith(".")) {
				fail(check, file, item.line, `browser modules must use local relative imports; found ${item.spec}`);
				continue;
			}
			if (!item.spec.endsWith(".js")) {
				fail(check, file, item.line, `local imports must include the .js extension: ${item.spec}`);
			}
			const target = resolveImport(file, item.spec);
			if (!existsSync(target)) {
				fail(check, file, item.line, `import target does not exist: ${item.spec}`);
				continue;
			}
			if (target === rel(file)) fail(check, file, item.line, "self-import is not allowed");
			if (!importAllowed(rel(file), target)) {
				fail(
					check,
					file,
					item.line,
					`${layerOf(file).label} layer must not import upward from ${layerOf(target).label}`
				);
			}
			deps.push(target);
		}
		graph.set(rel(file), deps);
	}
	const visiting = new Set();
	const visited = new Set();
	const stack = [];
	function dfs(node) {
		if (visiting.has(node)) {
			fail(
				check,
				node,
				null,
				`circular import detected: ${[...stack.slice(stack.indexOf(node)), node].join(" -> ")}`
			);
			return;
		}
		if (visited.has(node)) return;
		visiting.add(node);
		stack.push(node);
		for (const dep of graph.get(node) || []) dfs(dep);
		stack.pop();
		visiting.delete(node);
		visited.add(node);
	}
	for (const node of graph.keys()) dfs(node);
});

runCheck("links and route safety", (check) => {
	for (const file of files.filter((item) => [".js", ".html"].includes(ext(item)))) {
		const text = read(file);
		for (const entry of fileLineEntries(file, text)) {
			if (entry.line.includes("/#/")) fail(check, file, entry.number, "hash-router URLs are not allowed");
			if (/href=["']#["']/.test(entry.line)) {
				fail(check, file, entry.number, 'dead href="#" links are not allowed');
			}
			if (/href=["'][^"']+\.html["#'?]/.test(entry.line)) {
				fail(check, file, entry.number, "local links must use app routes, not .html files");
			}
		}
		for (const match of text.matchAll(/<a\b[\S\s]*?>/g)) {
			const tag = match[0];
			if (!/\btarget\s*=\s*["']_blank["']/.test(tag) || tag.includes("EXTERNAL_REL")) continue;
			const rel = tag.match(/\brel\s*=\s*(["'])(.*?)\1/);
			const relTokens = new Set((rel ? rel[2] : "").split(/\s+/).filter(Boolean));
			if (!relTokens.has("noopener") || !relTokens.has("nofollow")) {
				fail(
					check,
					file,
					lineNumber(text, match.index),
					'target="_blank" must include rel="noopener nofollow"'
				);
			}
		}
		for (const tag of text.match(/<img\b[^>]*>/g) || []) {
			if (!/\balt=/.test(tag)) {
				fail(check, file, lineNumber(text, text.indexOf(tag)), "img tags must include alt text");
			}
		}
	}
});

function cssWithoutComments(file) {
	return read(file).replaceAll(/\/\*[\S\s]*?\*\//g, "");
}

function designTokens() {
	const tokens = new Set();
	const colorValues = new Set();
	if (!existsSync(DESIGN_TOKEN_FILE)) return { tokens, colorValues };
	const text = cssWithoutComments(DESIGN_TOKEN_FILE);
	const definitionRegex = /(--[\w-]+)\s*:\s*([^;]+);/g;
	let match;
	while ((match = definitionRegex.exec(text))) {
		tokens.add(match[1]);
		for (const color of match[2].match(/#[\dA-Fa-f]{3,8}\b/g) || []) colorValues.add(color.toLowerCase());
	}
	return { tokens, colorValues };
}

function styleLinkOrder() {
	const html = read("public_html/index.html");
	return [...html.matchAll(/<link\b[^>]*href=["']([^"']+\.css)["'][^>]*>/g)].map((match) =>
		match[1].replace(/^\.\//, "")
	);
}

runCheck("design system drift", (check) => {
	const { tokens, colorValues } = designTokens();
	if (tokens.size === 0) fail(check, DESIGN_TOKEN_FILE, null, "design token source is missing or empty");
	const cssOrder = styleLinkOrder();
	if (cssOrder[0] !== "/styles/tokens.css") {
		fail(check, "public_html/index.html", null, "tokens.css must be the first stylesheet loaded");
	}
	for (const required of [
		"--color-surface",
		"--color-text",
		"--space-4",
		"--fs-body",
		"--radius-sm",
		"--focus-ring"
	]) {
		if (!tokens.has(required)) {
			fail(check, DESIGN_TOKEN_FILE, null, `required design token is missing: ${required}`);
		}
	}
	for (const file of files.filter(
		(item) => [".css", ".js", ".html"].includes(ext(item)) && rel(item).startsWith("public_html/")
	)) {
		const name = rel(file);
		const text = ext(file) === ".css" ? cssWithoutComments(file) : read(file);
		let match;
		const varRegex = /var\(\s*(--[\w-]+)/g;
		while ((match = varRegex.exec(text))) {
			const token = match[1];
			if (!tokens.has(token)) fail(check, file, lineNumber(text, match.index), `unknown design token ${token}`);
		}
		const definitionRegex = /(^|[\s;{])(--[\w-]+)\s*:/g;
		while ((match = definitionRegex.exec(text))) {
			const token = match[2];
			if (name !== DESIGN_TOKEN_FILE && !tokens.has(token)) {
				fail(
					check,
					file,
					lineNumber(text, match.index),
					`custom property ${token} must be registered in ${DESIGN_TOKEN_FILE}`
				);
			}
		}
		const colorRegex = /#[\dA-Fa-f]{3,8}\b|rgba?\([^)]+\)|hsla?\([^)]+\)/g;
		while ((match = colorRegex.exec(text))) {
			const literal = match[0].toLowerCase();
			if (name === DESIGN_TOKEN_FILE) continue;
			if (ext(file) === ".css") {
				fail(check, file, lineNumber(text, match.index), `raw colour ${literal} must be a design token`);
			} else if (!colorValues.has(literal)) {
				fail(
					check,
					file,
					lineNumber(text, match.index),
					`raw colour ${literal} is not registered in design tokens`
				);
			}
		}
	}
});

function splitSelectors(raw) {
	const selectors = [];
	let current = "";
	let depth = 0;
	let quote = "";
	for (const char of raw) {
		if (quote) {
			current += char;
			if (char === quote) quote = "";
			continue;
		}
		if (char === '"' || char === "'") {
			quote = char;
			current += char;
			continue;
		}
		if (char === "(" || char === "[") depth += 1;
		else if (char === ")" || char === "]") depth = Math.max(0, depth - 1);
		if (char === "," && depth === 0) {
			selectors.push(current.trim());
			current = "";
		} else {
			current += char;
		}
	}
	if (current.trim()) selectors.push(current.trim());
	return selectors;
}

runCheck("CSS selectors", (check) => {
	for (const file of files.filter((item) => ext(item) === ".css")) {
		const text = cssWithoutComments(file);
		const seen = new Map();
		const regex = /([^{}]+){([^{}]*)}/g;
		let match;
		while ((match = regex.exec(text))) {
			const raw = match[1].trim();
			if (!raw || raw.startsWith("@") || raw === "from" || raw === "to" || raw.endsWith("%")) continue;
			const body = match[2].trim().replaceAll(/\s+/g, " ");
			if (!body) continue;
			for (const selector of splitSelectors(raw).filter(Boolean)) {
				const key = `${selector} { ${body} }`;
				const first = seen.get(key);
				const line = lineNumber(text, match.index);
				if (first) {
					fail(check, file, line, `duplicate selector/body "${selector}" first appeared on line ${first}`);
				} else {
					seen.set(key, line);
				}
			}
		}
	}
});

function escapeRegex(value) {
	return value.replaceAll(/[$()*+.?[\\\]^{|}]/g, "\\$&");
}

function identifierOccurrences(text, name) {
	return [...text.matchAll(new RegExp(`(?<![A-Za-z0-9_$])${escapeRegex(name)}(?![A-Za-z0-9_$])`, "g"))].length;
}

function browserModuleGraph() {
	const browserFiles = files
		.filter((item) => JS_EXTS.has(ext(item)) && rel(item).startsWith("public_html/"))
		.map((file) => rel(file));
	const graph = new Map(browserFiles.map((file) => [file, []]));
	for (const file of browserFiles) {
		for (const item of importsFor(file)) {
			if (!item.spec.startsWith(".")) continue;
			const target = resolveImport(file, item.spec);
			if (graph.has(target)) graph.get(file).push(target);
		}
	}
	return graph;
}

function reachableModules(graph, entry) {
	const seen = new Set();
	function visit(node) {
		if (seen.has(node) || !graph.has(node)) return;
		seen.add(node);
		for (const dep of graph.get(node)) visit(dep);
	}
	visit(entry);
	return seen;
}

function classNamesInSource() {
	const names = new Set();
	for (const file of files.filter(
		(item) => [".js", ".html"].includes(ext(item)) && rel(item).startsWith("public_html/")
	)) {
		for (const match of read(file).matchAll(/[A-Za-z][\w-]*(?:__[A-Za-z][\w-]*)?(?:--[A-Za-z][\w-]*)?/g)) {
			names.add(match[0]);
		}
	}
	return names;
}

runCheck("dead code", (check) => {
	const graph = browserModuleGraph();
	const reachable = reachableModules(graph, APP_ENTRY);
	for (const file of graph.keys()) {
		if (!reachable.has(file)) fail(check, file, null, "browser module is not reachable from public_html/main.js");
	}
	const referencedClasses = classNamesInSource();
	for (const file of files.filter((item) => ext(item) === ".css" && rel(item).startsWith("public_html/styles/"))) {
		const text = cssWithoutComments(file);
		for (const match of text.matchAll(/\.([A-Za-z][\w-]*)/g)) {
			const name = match[1];
			if (name.startsWith("is-") || name.startsWith("has-") || name.startsWith("on-")) continue;
			if (!referencedClasses.has(name)) {
				fail(
					check,
					file,
					lineNumber(text, match.index),
					`CSS class .${name} is not referenced outside stylesheets`
				);
			}
		}
	}
	for (const file of files.filter((item) => JS_EXTS.has(ext(item)) && rel(item).startsWith("public_html/"))) {
		const text = read(file);
		for (const match of text.matchAll(/export\s+function\s+([$A-Z_a-z][\w$]*)/g)) {
			const name = match[1];
			if (identifierOccurrences(text, name) === 1) {
				let imported = false;
				for (const other of files.filter((item) => JS_EXTS.has(ext(item)) && item !== file)) {
					if (read(other).includes(name)) {
						imported = true;
						break;
					}
				}
				if (!imported) {
					fail(check, file, lineNumber(text, match.index), `exported function ${name} appears unused`);
				}
			}
		}
	}
});

function normalizeDuplicateLine(line) {
	return line
		.trim()
		.replaceAll(/(["'`])(?:\\.|(?!\1).)*\1/g, '""')
		.replaceAll(/\b\d+(?:\.\d+)?\b/g, "0")
		.replaceAll(/\s+/g, " ");
}

runCheck("duplicate blocks", (check) => {
	const seen = new Map();
	for (const file of files.filter((item) => [".js", ".mjs", ".css", ".html"].includes(ext(item)))) {
		const normalized = read(file)
			.split(/\r?\n/)
			.map((line) => normalizeDuplicateLine(line));
		for (let index = 0; index <= normalized.length - DUPLICATE_WINDOW; index += 1) {
			const window = normalized.slice(index, index + DUPLICATE_WINDOW);
			if (window.some((line) => !line || line === "}" || line === "};" || line.startsWith("import "))) continue;
			if (window.join("").length < 240) continue;
			const key = window.join("\n");
			const previous = seen.get(key);
			if (previous && (previous.file !== file || previous.line !== index + 1)) {
				fail(
					check,
					file,
					index + 1,
					`duplicate ${DUPLICATE_WINDOW}-line block first appeared in ${previous.file}:${previous.line}`
				);
			} else {
				seen.set(key, { file, line: index + 1 });
			}
		}
	}
});

function functionRanges(text) {
	const ranges = [];
	const regex = /function\s+([$A-Z_a-z][\w$]*)\s*\(([^)]*)\)\s*{/g;
	let match;
	while ((match = regex.exec(text))) {
		let depth = 0;
		let end = match.index;
		for (let i = match.index; i < text.length; i += 1) {
			if (text[i] === "{") {
				depth += 1;
			} else if (text[i] === "}") {
				depth -= 1;
				if (depth === 0) {
					end = i + 1;
					break;
				}
			}
		}
		ranges.push({ name: match[1], params: match[2], start: match.index, end });
	}
	return ranges;
}

runCheck("code shape budgets", (check) => {
	for (const file of files.filter((item) => JS_EXTS.has(ext(item)))) {
		const text = read(file);
		for (const fn of functionRanges(text)) {
			const body = text.slice(fn.start, fn.end);
			const lines = body.split(/\r?\n/).length;
			const branches = (body.match(/\b(if|for|while|case|catch|\?|&&|\|\|)\b/g) || []).length;
			const params = fn.params
				.split(",")
				.map((item) => item.trim())
				.filter(Boolean).length;
			if (lines > FUNCTION_LIMITS.lines) {
				fail(check, file, lineNumber(text, fn.start), `${fn.name} has ${lines} lines`);
			}
			if (branches > FUNCTION_LIMITS.branches) {
				fail(check, file, lineNumber(text, fn.start), `${fn.name} has ${branches} branches`);
			}
			if (params > FUNCTION_LIMITS.params) {
				fail(check, file, lineNumber(text, fn.start), `${fn.name} has ${params} parameters`);
			}
		}
	}
});

function normalizeFunctionBody(lines) {
	return lines
		.map((line) => normalizeDuplicateLine(line))
		.filter((line) => line && line !== "}" && line !== "};")
		.join("\n")
		.replaceAll(/\b[$A-Z_a-z][\w$]*\b/g, (word) =>
			[
				"if",
				"else",
				"for",
				"while",
				"return",
				"const",
				"let",
				"var",
				"function",
				"await",
				"async",
				"new"
			].includes(word)
				? word
				: "id"
		);
}

runCheck("duplicate functions", (check) => {
	const seen = new Map();
	for (const file of files.filter((item) => JS_EXTS.has(ext(item)))) {
		const text = read(file);
		for (const fn of functionRanges(text)) {
			const body = normalizeFunctionBody(text.slice(fn.start, fn.end).split(/\r?\n/));
			if (body.length < 240) continue;
			const previous = seen.get(body);
			if (previous) {
				fail(
					check,
					file,
					lineNumber(text, fn.start),
					`${fn.name} duplicates ${previous.file}:${previous.line}`
				);
			} else {
				seen.set(body, { file, line: lineNumber(text, fn.start) });
			}
		}
	}
});

